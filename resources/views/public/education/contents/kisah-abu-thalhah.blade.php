        <p>
           Dikisahkan, Abu Thalhah (Zaid bin Sahl) seorang sahabat dari kalangan Anshar yang memiliki kebun bernama 
           Bairuha’ yang terletak tidak jauh dari Masjid Madinah, sebagai harta paling dicintai dan dibanggakannya.
        </p>

        <p>
           Suatu waktu, Rasulullah SAW biasa masuk ke dalamnya dan berteduh di sana serta minum dari airnya. 
           Tak lama setelah kejadian itu turun ayat Alquran yang berbunyi:
        </p>

        <p>
          “Sekali-kali kamu tidak sampai pada kebaikan (yang sempurna) sebelum kamu menafkahkan 
          sebagian harta yang kamu cintai.” (Ali Imran: 92).
        </p>

        <p>
          Setelah ayat diatas sampai ke telinga Abu Thalhah (Zaid bin Sahl). Kemudian, Ia bergegas mendatangi Rasulullah 
          SAW dan berkata, “Aku ingin mengamalkan apa yang diperintahkan Allah untuk menyedekahkan apa yang kita cintai, 
          wahai Rasulullah. Dengan harapan mendapatkan kebaikan sekaligus sebagai simpanan di sisi Allah. Maka ambillah 
          dan letakkan ia di tempat yang pantas menurutmu. Terimalah kebun Bairuha’, satu-satunya harta yang aku miliki, 
          sebagai sedekah. Aku serahkan kepada Anda untuk dibagi-bagikan kepada orang yang mem butuhkan.”
        </p>

        <p>
          Dengan gembira dan penuh sukacita, Rasulullah menyambut sedekah itu dan menguasakan teknis pembagian kebun itu 
          kepada Abu Thalhah sendiri dan sambil berkata, ”Inilah harta yang diberkahi. Aku telah mendengar apa yang kau 
          ucapkan dan aku menerimanya. Aku kembalikan lagi kepadamu dan berikanlah ia kepada kerabat-kerabat terdekatmu.”
        </p>

        <p>
          Rasulullah hanya menyarankan agar harta itu dibagikan kepada keluarga Abu Thalhah yang terdekat dan sangat 
          membutuhkan terlebih dulu, baru kepada orang lain.
        </p>

        <p>
          Selain itu, Abu Thalhah juga memberikan bagian kepada Rasulullah. Kemudian Rasulullah memberikan bagiannya 
          tersebut kepada seorang penyair, Hassan bin Tsabit al-Anshari.  Serta di antara orang yang menerima lainnya 
          adalah Zaid bin Tsabit dan Ubay bin Ka’ab.
        </p>
    </div>


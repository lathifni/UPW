        <p>
            Dalam sejarah Islam, Wakaf dikenal sejak masa Rasulullah SAW karena wakaf disyariatkan setelah nabi SAW Madinah,
            pada tahun kedua Hijriyah. Ada dua pendapat yang berkembang di kalangan ahli yurisprudensi Islam (fuqaha’) tentang
            siapa yang pertama kali melaksanakan syariat wakaf.
        </p>

        <p>
           Menurut sebagian pendapat ulama mengatakan bahwa yang pertama kali melaksanakan wakaf adalah Rasulullah SAW ialah wakaf
           tanah milik Nabi SAW untuk dibangun masjid.
           Pendapat ini berdasarkan hadits yang diriwayatkan oleh Umar bin Syabah dari ‘Amr bin Sa’ad bin Mu’ad, ia berkata: Dan
           diriwayatkan dari Umar bin Syabah, dari Umar bin Sa’ad bin Muad berkata: “Kami bertanya tentang mula-mula wakaf dalam Islam?
           Orang Muhajirin mengatakan adalah wakaf Umar, sedangkan orang-orang Ansor mengatakan adalah wakaf Rasulullah SAW.”
           (Asy-Syaukani: 129).
           Rasulullah SAW pada tahun ketiga Hijriyah pernah mewakafkan ketujuh kebun kurma di Madinah; diantaranya ialah kebon A’raf,
           Shafiyah, Dalal, Barqah dan kebon lainnya.
        </p>

        <p>
          Menurut pendapat sebagian ulama mengatakan bahwa yang pertama kali melaksanakan Syariat Wakaf adalah Umar bin Khatab.
          Pendapat ini berdasarkan hadits yang diriwayatkan Ibnu Umar ra, ia berkata: Dari Ibnu Umar ra, berkata : “Bahwa sahabat Umar ra,
          memperoleh sebidang tanah di Khaibar, kemudian Umar ra, menghadap Rasulullah SAW untuk meminta petunjuk, Umar berkata : “Hai Rasulullah
          SAW., saya mendapat sebidang tanah di Khaibar, saya belum mendapat harta sebaik itu, maka apakah yang engkau perintahkan kepadaku?”
          Rasulullah SAW. bersabda: “Bila engkau suka, kau tahan (pokoknya) tanah itu, dan engkau sedekahkan (hasilnya), tidak dijual, tidak
          dihibahkan dan tidak diwariskan.
          Ibnu Umar berkata: “Umar menyedekahkannya (hasil pengelolaan tanah) kepada orang-rang fakir, kaum kerabat, hamba sahaya, sabilillah,
          Ibnu sabil dan tamu.
          Dan tidak dilarang bagi yang mengelola (nazhir) wakaf makan dari hasilnya dengan cara yang baik (sepantasnya) atau memberi makan orang
          lain dengan tidak bermaksud menumpuk harta” (HR.Muslim).
          Kemudian syariat wakaf yang telah dilakukan oleh Umar bin Khatab dususul oleh Abu Thalhah yang mewakafkan kebun kesayangannya, kebun
          “Bairaha”.
          Selanjutnya disusul oleh sahabat Nabi SAW. lainnya, seperti Abu Bakar yang mewakafkan sebidang tanahnya di Mekkah yang diperuntukkan
          kepada anak keturunannya yang datang ke Mekkah.
          Utsman menyedekahkan hartanya di Khaibar.
          Ali bin Abi Thalib mewakafkan tanahnya yang subur.
          Mu’ads bin Jabal mewakafkan rumahnya, yang populer dengan sebutan “Dar Al-Anshar”.
          Kemudian pelaksanaan wakaf disusul oleh Anas bin Malik, Abdullah bin Umar, Zubair bin Awwam dan Aisyah Isri Rasulullah SAW.
        </p>

        <p>
          Praktek wakaf menjadi lebih luas pada masa dinasti Umayah dan dinasti Abbasiyah, semua orang berduyun-duyun untuk melaksanakan
          wakaf, dan wakaf tidak hanya untuk orang-orang fakir dan miskin saja, tetapi wakaf menjadi modal untuk membangun lembaga
          pendidikan, membangun perpustakaan dan membayar gaji para statnya, gaji para guru dan beasiswa untuk para siswa dan mahasiswa.
          Antusiasme masyarakat kepada pelaksanaan wakaf telah menarik perhatian negara untuk mengatur pengelolaan wakaf sebagai sektor
          untuk membangun solidaritas sosial dan ekonomi masyarakat.
          Wakaf pada mulanya hanyalah keinginan seseorang yang ingin berbuat baik dengan kekayaan yang dimilikinya dan dikelola secara
          individu tanpa ada aturan yang pasti.
          Namun setelah masyarakatIslam merasakan betapa manfaatnya lembaga wakaf, maka timbullah keinginan untuk mengatur perwakafan
          dengan baik.
          Kemudian dibentuk lembaga yang mengatur wakaf untuk mengelola, memelihara dan menggunakan harta wakaf, baik secara umum seperti
          masjid atau secara individu atau keluarga.
          Pada masa dinasti Umayyah yang menjadi hakim Mesir adalah Taubah bin Ghar Al-Hadhramiy pada masa khalifah Hisyam bin Abd. Malik.
          Ia sangat perhatian dan tertarik dengan pengembangan wakaf sehingga terbentuk lembaga wakaf tersendiri sebagaimana lembaga
          lainnya dibawah pengawasan hakim.
          Lembaga wakaf inilah yang pertama kali dilakukan dalam administrasi wakaf di Mesir, bahkan diseluruh negara Islam.
          Pada saat itu juga, Hakim Taubah mendirikan lembaga wakaf di Basrah.
          Sejak itulah pengelolaan lembaga wakaf di bawah Departemen Kehakiman yang dikelola dengan baik dan hasilnya disalurkan kepada yang
          berhak dan yang membutuhkan.
          Pada masa dinasti Abbasiyah terdapat lembaga wakaf yang disebut dengan “shadr al-Wuquuf” yang mengurus administrasi dan memilih
          staf pengelola lembaga wakaf.
          Demikian perkembangan wakaf pada masa dinasti Umayyah dan Abbasiyah yang manfaatnya dapat dirasakan oleh masyarakat, sehingga
          lembaga wakaf berkembang searah dengan pengaturan administrasinya.
        </p>

        <p>
          Pada masa dinasti Ayyubiyah di Mesir perkembangan wakaf cukup menggembirakan, dimana hampir semua tanah-tanah pertanian menjadi
          harta wakaf dan semua dikelola oleh negara dan menjadi milik negara (baitul mal).
          Ketika Shalahuddin Al-Ayyuby memerintah Mesir, maka ia bermaksud mewakafkan tanah-tanah milik negara diserahkan kepada yayasan
          keagamaan dan yayasan sosial sebagaimana yang dilakukan oleh dinasti Fathimiyah sebelumnnya, meskipun secara fiqh Islam hukum
          mewakafkan harta baitulmal masih berbeda pendapat di antara para ulama.
          Pertama kali orang yang mewakafkan tanah milik nagara (baitul mal) kepada yayasan dan sosial adalah Raja Nuruddin Asy-Skyahid
          dengan ketegasan fatwa yang dekeluarkan oleh seorang ulama pada masa itu ialah Ibnu “Ishrun dan didukung oleh pada ulama lainnya
          bahwa mewakafkan harta milik negara hukumnya boleh (jawaz), dengan argumentasi (dalil) memelihara dan menjaga kekayaan negara.
          Sebab harta yang menjadi milik negara pada dasarnya tidak boleh diwakafkan.
          Shalahuddin Al-Ayyubi banyak mewakafkan lahan milik negara untuk kegiatan pendidikan, seperti mewakafkan beberapa desa (qaryah)
          untuk pengembangan madrasah mazhab asy-Syafi’iyah, madrasah al-Malikiyah dan madrasah mazhab al-Hanafiyah dengan dana melalui
          model mewakafkan kebun dan lahan pertanian, seperti pembangunan madrasah mazhab Syafi’iy di samping kuburan Imam Syafi’I dengan
          cara mewakafkan kebun pertanian dan pulau al-Fil.
          Dalam rangka mensejahterakan ulama dan kepentingan misi mazhab Sunni Shalahuddin al-Ayyuby menetapkan kebijakan (1178 M/572 H)
          bahwa bagi orang Kristen yang datang dari Iskandar untuk berdagang wajib membayar bea cukai.
          Hasilnya dikumpulkan dan diwakafkan kepada para ahli yurisprudensi (fuqahaa’) dan para keturunannya.
          Wakaf telah menjadi sarana bagi dinasti al-Ayyubiyah untuk kepentingan politiknya dan misi alirannya ialah mazhab Sunni dan
          mempertahankan kekuasaannya.
          Dimana harta milik negara (baitul mal) menjadi modal untuk diwakafkan demi pengembangan mazhab Sunni dan menggusus mazhab Syi’ah
          yang dibawa oleh dinasti sebelumnya, ialah dinasti Fathimiyah.
        </p>

        <p>
          Perkembangan wakaf pada masa dinasti Mamluk sangat pesat dan beraneka ragam, sehingga apapun yang dapat diambil manfaatnya boleh
          diwakafkan.
          Akan tetapi paling banyak yang diwakafkan pada masa itu adalah tanah pertanian dan bangunan, seperti gedung perkantoran, penginapan
          dan tempat belajar.
          Pada masa Mamluk terdapat wakaf hamba sahaya yang di wakafkan budak untuk memelihara masjid dan madrasah.
          Hal ini dilakukan pertama kali oleh pengusa dinasti Ustmani ketika menaklukan Mesir, Sulaiman Basya yang mewakafkan budaknya
          untuk merawat mesjid.
          Manfaat wakaf pada masa dinasti Mamluk digunakan sebagaimana tujuan wakaf, seperti wakaf keluarga untuk kepentingan keluarga,
          wakaf umum untuk kepentingan sosial, membangun tempat untuk memandikan mayat dan untuk membantu orang-orang fakir dan miskin.
          Yang lebih membawa syiar islam adalah wakaf untuk sarana Harmain, ialah Mekkah dan Madinah, seperti kain ka’bah (kiswatul ka’bah).
          Sebagaimana yang dilakukan oleh Raja Shaleh bin al-Nasir yang membrli desa Bisus lalu diwakafkan untuk membiayai kiswah Ka’bah
          setiap tahunnya dan mengganti kain kuburan Nabi SAW dan mimbarnya setiap lima tahun sekali.
          Perkembangan berikutnya yang dirasa manfaat wakaf telah menjadi tulang punggung dalam roda ekonomi pada masa dinasti Mamluk
          mendapat perhatian khusus pada masa itu meski tidak diketahui secara pasti awal mula disahkannya undang-undang wakaf.
          Namun menurut berita dan berkas yang terhimpun bahwa perundang-undangan wakaf pada dinasti Mamluk dimulai sejak Raja al-Dzahir
          Bibers al-Bandaq (1260-1277 M/658-676) H) di mana dengan undang-undang tersebut Raja al-Dzahir memilih hakim dari masing-masing
          empat mazhab Sunni.
          Pada orde al-Dzahir Bibers perwakafan dapat dibagi menjadi tiga katagori: Pendapat negara hasil wakaf yang diberikan oleh penguasa
          kepada orang-orang yanbg dianggap berjasa, wakaf untuk membantu haramain (fasilitas Mekkah dan Madinah) dan kepentingan masyarakat
          umum.
        </p>

        <p>
          Sejak abad lima belas, kerajaan Turki Utsmani dapat memperluas wilayah kekuasaannya, sehingga Turki dapat menguasai sebagian
          besar wilayah negara Arab.
          Kekuasaan politik yang diraih oleh dinasti Utsmani secara otomatis mempermudah untuk merapkan Syari’at Islam, diantaranya ialah
          peraturan tentang perwakafan.
          Di antara undang-undang yang dikeluarkan pada dinasti Utsmani ialah peraturan tentang pembukuan pelaksanaan wakaf, yang dikeluarkan
          pada tanggal 19 Jumadil Akhir tahun 1280 Hijriyah.
          Undang-undang tersebut mengatur tentang pencatatan wakaf, sertifikasi wakaf, cara pengelolaan wakaf, upaya mencapai tujuan wakaf
          dan melembagakan wakaf dalam upaya realisasi wakaf dari sisi administrasi dan perundang-udangan.
          Pada tahun 1287 Hijriyah dikeluarkan undang-undang yang menjelaskan tentang kedudukan tanah-tanah kekuasaan Turki Utsmani dan
          tanah-tanah produktif yang berstatus wakaf.
          Dari implementasi undang-undang tersebut di negara-negara Arab masih banyak tanah yang berstatus wakaf dan diperaktekkan sampai
          saat sekarang.
        </p>

        <p>
          Sejak masa Rasulullah, masa kekhalifahan dan masa dinasti-dinasti Islam sampai sekarang wakaf masih dilaksanakan dari waktu ke
          waktu di seluruh negeri muslim, termasuk di Indonesia.
          Hal ini terlihat dari kenyataan bahwa lembaga wakaf yang berasal dari agama Islam ini telah diterima (diresepsi) menjadi hukum
          adat bangsa Indonesia sendiri.
          Disamping itu suatu kenyataan pula bahwa di Indonesia terdapat banyak benda wakaf, baik wakaf benda bergerak atau benda tak
          bergerak.
          Kalau kita perhatikan di negara-negara muslim lain, wakaf mendapat perhatian yang cukup sehingga wakaf menjadi amal sosial yang
          mampu memberikan manfaat kepada masyarakat banyak.
          Dalam perjalanan sejarah wakaf terus berkembang dan akan selalu berkembang bersamaan dengan laju perubahan jaman dengan berbagai
          inovasi-inovasi yang relevan, seperti bentuk wakaf uang, wakaf Hak Kekayaan Intelektual (Haki), dan lain-lain.
          Di Indonesia sendiri, saat ini wakaf kian mendapat perhatian yang cukup serius dengan diterbitkannya Undang-undang No. 41 tahun
          2004 tentang Wakaf dan PP No. 42 tahun 2006 tentang pelaksanaannya.
        </p>

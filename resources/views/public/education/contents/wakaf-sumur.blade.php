        <p>
           Suatu ketika saat 1400 abad lalu dimasa Nabi Muhammad SAW, Kota Madinah pernah mengalami paceklik hingga 
           kesulitan air bersih. Satu-satunya sumber air yang tersisa adalah sebuah sumur milik seorang Yahudi, 
           yaitu Sumur Raumah. Rasa airnya mirip dengan sumur zam-zam. Kaum muslimin dan penduduk Madinah terpaksa 
           harus rela antri dan membeli air bersih dari Yahudi tersebut.
        </p>

        <p>
           Prihatin atas kondisi umatnya, Rasulullah kemudian bersabda, “Wahai Sahabatku, siapa saja di antara 
           kalian yang menyumbangkan hartanya untuk dapat membebaskan sumur itu, lalu menyumbangkannya untuk umat, 
           maka akan mendapat surga-Nya Allah Ta’ala,” demikian hadis riwayat (HR. Muslim). Mendengar hal itu, 
           Utsman bin Affan yang kemudian segera bergerak untuk membebaskan Sumur Raumah itu. Utsman segera 
           mendatangi Yahudi pemilik sumur dan menawar untuk membeli sumur Raumah dengan harga yang tinggi.
        </p>

        <p>
          Walau sudah diberi penawaran yang tertinggi sekali pun, Yahudi pemilik sumur tetap menolak menjualnya, 
          “Seandainya sumur ini saya jual kepadamu wahai Utsman, maka aku tidak memiliki penghasilan yang bisa 
          aku peroleh setiap hari,” demikian Yahudi tersebut menjelaskan alasan penolakannya.
        </p>

        <p>
          Utsman bin Affan yang ingin sekali mendapatkan balasan pahala berupa surga tidak kehilangan cara 
          mengatasi penolakan Yahudi ini. Ia pun membeli setengah sumur itu dan memilikinya secara bergantian. 
          Akhirnya pemilik sebelumnya setuju menerima tawaran Utsman tadi dan disepakati pula hari itu juga 
          separuh dari Sumur Raumah adalah milik Utsman. Utsman pun segera mengumumkan kepada penduduk Madinah 
          yang mau mengambil air di Sumur Raumah untuk mengambil air dengan gratis karena hari ini sumur Raumah 
          adalah miliknya. Seraya ia mengingatkan agar penduduk Madinah mengambil air dalam jumlah yang cukup 
          untuk dua hari, karena esok hari sumur itu bukan lagi milik Utsman. Keesokan hari Yahudi mendapati 
          sumur miliknya sepi pembeli, karena penduduk Madinah masih memiliki persedian air di rumah. Yahudi 
          itupun mendatangi Utsman dan menawarkan untuk menjual sumur itu dengan harga yang sama. Utsman yang 
          setuju lalu membelinya seharga 20.000 dirham, maka sumur Raumah pun menjadi milik Utsman seutuhnya.
          Kemudian Utsman bin Affan mewakafkan Sumur Raumah. Sejak saat itu Sumur Raumah dapat dimanfaatkan 
          oleh siapa saja, termasuk orang Yahudi pemilik lamanya.
        </p>

        <p>
          Wakaf sumur Utsman kini terus berkembang. Oleh pemerintah Ustmaniyah, wakaf Ustman dijaga dan 
          dikembangkan. Perawatan wakaf Ustman ini dilanjutkan Kerajaan Saudi. Alhasil, dikebun tersebut tumbuh 
          sekitar 1550 pohon kurma. Kerajaan Arab melalui kementerian Pertanian mengelola hasil kebun wakaf Utsman.
          Uang yang didapat dari panen kurma dibagi dua, setengahnya dibagikan kepada anak-anak yatim dan fakir 
          miskin lalu separuhnya lagi disimpan di sebuah bank dengan rekening atas nama Utsman bin Affan.
        </p>

        <p>
          Rekening atas nama Utsman bin Affan dipegang oleh Kementerian Wakaf. Dengan begitu ‘kekayaan’ Utsman bin 
          Affan yang tersimpan di bank terus bertambah sampai pad aakhirnya digunakan untuk membeli sebidang tanah 
          di kawasan Markaziyah (area eksklusif) dekat Masjid Nabawi. Diatas tanah itulah hotel Utsman bin Affam 
          dibangun dari uang rekeningnya, tepat disamping Majsid yang juga atas nama Utsman bin Affan.
        </p>

        <p>
          Hotel tersebut kini dikelola oleh Sheraton dan salah satu hotel bertaraf internasional. Hotel tersebut 
          berdiri gagah setinggi 15 lantai dengan 24 kamar disetiap lantai. Hotel tersebut dilengkapi dengan restoran 
          besar dan tempat belanja. Dekat hotel tersebut terdapat Masjid Utsman bin Affan yang juga masih aktif digunakan.
        </p>

        <p>
          Inilah contoh wakaf yang produktif dan terus mengalir amalnya sampai nanti. Bahkan bukan manfaatnya yang terus 
          mengalir melainkan juga amalnya. Dalam kasus wakaf Utsman ini, amal akan mengalir terus sampai hari akhir ke 
          kitab amal shaleh atas nama Utsman bin Affan.
        </p>

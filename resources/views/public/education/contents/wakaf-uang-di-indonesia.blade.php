         <p>
            Sejarah awal mula wakaf dimulai pada masa Rasulullah SAW, yaitu pada tahun kedua Hijriyah setelah Nabi hijrah ke Madinah. Wakaf disyariatkan untuk memberikan manfaat kepada masyarakat secara berkelanjutan. Ada dua pendapat yang berkembang di kalangan ahli yurisprudensi Islam (fuqaha) tentang siapa yang pertama kali melaksanakan syariat wakaf. Menurut sebagian ulama, yang pertama kali melaksanakan wakaf adalah Rasulullah SAW, yakni mewakafkan tanah milik Nabi SAW untuk dibangun masjid.
        <p>
          Wakaf uang di Indonesia mulai dikenal sejak dikeluarkannya fatwa wakaf uang oleh DSN MUI pada 2012.
          Fatwa itu berisi lima point penting. Pertama, Wakaf Uang (Cash Wakaf/Wagf al-Nuqud) adalah wakaf yang
          dilakukan seseorang, kelompok orang, lembaga atau badan hukum dalam bentuk uang tunai. Kedua, termasuk
          ke pengertian uang adalah surat-surat berharga. Ketiga, wakaf uang hukumnya jawaz (boleh), sedangkan
          keempat, wakaf uang hanya boleh disalurkan dan digunakan untuk hal-hal yang dibolehkan secara syar’i
          dan kelima, nilai pokok Wakaf Uang harus dijamin kelestariannya, tidak boleh dijual, dihibahkan, dan
          atau diwariskan.
        </p>

        <p>
           Di Bangladesh, wakaf uang mulai dikenal tahun 1998.  M.A. Mannan orang yang pertama kali mengenalkannya
           melalui SIBL (Social Islamic Bank Limited).  SIBL mengeluarkan sertifikat wakaf uang yang pertama dalam
           sejarah perbankan.  Wakif mendepositokan uangnya ke rekening wakaf uang.  Lalu, bank mengelola uang
           yang didepositokan tersebut atas nama wakif.  Hasil pengelolaan tersebut akan diberikan kepada mauquf
           alaih.  Tidak heran kalau SIBL juga memiliki rumah sakit yang dikelola dari hasil wakaf uangnya.
        </p>

        <p>
          Di Turki, wakaf uang mulai dikenal abad ke 15 Masehi.  Sejak 400 tahun yang lalu , praktik wakaf uang
          ini telah menjadi trend di kalangan masyarakat.  Pengadilan Ottoman telah menyetujui praktek waqaf uang
          pada abad ke 15.  Jenis wakaf ini kemudian menjadi sangat populer pada abad ke 16 di seluruh Anatolia
          dan daratan Eropa dari kerajaan Ottoman, Turki.  Pada zaman Ottoman, waqaf uang ini dipraktekkan hampir
          300 tahun, dimulai dari tahun 1555-1823 M.  Lebih dari 20 persen waqaf uang di Kota Bursa, selatan
          Istanbul, telah bertahan lebih dari seratus tahun.  Dalam pengelolaannya, hanya 19 persen waqaf uang
          yang tidak bertambah, sementara 81 persen mengalami pertambahan (akumulasi) modal.  Pada bulan safar,
          1513 M, Elhac Sulaymen mewaqafkan 70.000 dirham perak.  40.000 dirham digunakan untuk membangun sekolah,
          dan 30.000 dirham lagi digunakan untuk pembiayaan murabahah.  Hasil investasi murabahah ini, digunakan
          untuk membayar gaji guru sebesar 3 dirham per hari, asisten 1 dirham, qori pembaca Al-qur’an 1 dirham,
          dan nazir, pengelola waqaf, 2 dirham setiap harinya.
        </p>

        <p>
          Di dunia, wakaf uang pertama sekali dikenalkan oleh Imam Al Zuhri (wafat 124 H).  Beliau mengatakan
          bahwa mewakafkan dinar hukumnya boleh, dengan cara menjadikan dinar tersebut sebagai modal usaha kemudian
          keuntungannya disalurkan pada mauquf ‘alaih.  Dengan semangat ini, maka wakaf sejatinya adalah produktif
          dan berfungsi sebagai sumber dana pembangunan ekonomi.  Oleh karena itu, dalam UU no. 41 tahun 2004
          tentang wakaf, diakui keberadaan wakaf uang di Indonesia.
        </p>

        <p>
          Dalam peraturan BWI no. 1 tahun 2020 tentang pedoman pengelolaan dan pengembangan harta benda wakaf,
          diatur lagi tentang wakaf uang agar lebih memberikan manfaat sebesar besarnya bagi ekonomi mulai pasal
          5 sampai pasal 19.  Dalam pasal 12 ayat 1 dijelaskan bahwa nazir wajib membedakan pengelolaan antara wakaf
          uang untuk jangka waktu tertentu dengan wakaf uang untuk waktu selamanya.  Wakaf uang untuk jangka waktu
          tertentu contohnya adalah Cash waqf linked sukuk, Kalisa dan akbari.
        </p>
